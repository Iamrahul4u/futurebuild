export default async function Page({ params }: { params: { userId: string } }) {
  const userDetails: UserWithJobs | null = await prisma.user.findFirst({
    where: {
      id: params.userId[0],
    },
    include: {
      postedJobs: {
        select: {
          id: true,
        },
      },
    },
  });
  const jobDetails = await prisma.jobPost.findMany({
    where: {
      id: {
        in: userDetails?.postedJobs.map((jobs) => jobs.id) ?? [],
      },
    },
    select: {
      id: true,
      jobTitle: true,
      _count: {
        select: {
          applicants: true,
        },
      },
    },
  });
  if (!userDetails) {
    redirect("/authenticate/signin");
  }
  return (
    <div className="grid gap-4 lg:grid-cols-2">
      <Card>
        <CardHeader>
          <CardTitle>Account Details</CardTitle>
        </CardHeader>
        <CardContent className="grid gap-4">
          <div className="flex gap-2">
            <div className="text-muted-foreground">Name:</div>
            {userDetails.firstName}
            &nbsp;{userDetails?.secondName}
          </div>
          <div className="flex gap-2">
            <div className="text-muted-foreground">Email:</div>
            <div>{userDetails.email}</div>
          </div>

          <div className="flex gap-2">
            <div className="text-muted-foreground">Phone:</div>
            <div>Phone</div>
          </div>
          <div className="flex gap-2">
            <div className="text-muted-foreground">Address:</div>
            <div>123 Main St, Anytown USA</div>
          </div>
          <Button variant="outline" className="justify-self-end">
            Edit Profile
          </Button>
        </CardContent>
      </Card>
      <div>
        <CardHeader>
          <CardTitle>Posted Jobs</CardTitle>
        </CardHeader>
        <TableList details={jobDetails} />
      </div>
      {userDetails.role === "USER" && (
        <Card>
          <CardHeader>
            <CardTitle>Jobs Applied</CardTitle>
          </CardHeader>
          <CardContent className="grid gap-4">
            <div className="flex items-center justify-between">
              <div>
                <div className="text-4xl font-bold">7</div>
                <div className="text-muted-foreground">Jobs Applied</div>
              </div>
              <Link href="#" className="text-primary" prefetch={false}>
                View Details
              </Link>
            </div>
          </CardContent>
        </Card>
      )}
    </div>
  );
}

